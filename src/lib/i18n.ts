const TABLES: Record<Locale, Dict> = { ru, en };

export function translate(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>,
): string {
  const table = TABLES[locale] ?? TABLES.ru;
  let s = table[key] ?? TABLES.ru[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

export function expertiseLabel(locale: Locale, exp: Expertise): string {
  return translate(locale, `onb.exp.${exp}`);
}

export function normalizeLocale(v: string | null | undefined): Locale {
  return v === "en" ? "en" : "ru";
}

export function normalizeExpertise(v: string | null | undefined): Expertise {
  if (v === "beginner" || v === "intermediate" || v === "expert") return v;
  return "beginner";
}
